export function buildBombBlastLevel(level: number): { wall: boolean[]; cols: number } {
  const spec = BOMB_BLAST_LEVELS[Math.min(level, BOMB_BLAST_LEVELS.length) - 1];
  const cols = spec.pattern[0].length;
  const wall: boolean[] = [];
  for (const row of spec.pattern) {
    for (let x = 0; x < cols; x++) {
      wall.push(row[x] === '#');
    }
  }
  return { wall, cols };
}
